    ArrayList presentar() {
        try {
            String query = "Select * from Registro";
            presentar = conexion.prepareStatement(query);
            ResultSet resultado = presentar.executeQuery();
            while (resultado.next()) {
//                Libro libros = new Libro();
                String numeroCuenta = resultado.getString("numeroCuenta");
                String cliente = resultado.getString("cliente");
                String tipoCuenta = resultado.getString("tipoCuenta");
                String movimiento = resultado.getString("movimiento");
                String fecha = resultado.getString("fecha");
                String saldo = resultado.getString("Saldo");

                Cuenta libros = new Cuenta(numeroCuenta, cliente, tipoCuenta, movimiento, fecha, saldo);
                registro.add(libros);
            }
            for (int i = 0; i < registro.size(); i++) {
                System.out.println(registro.get(i).getCliente());
            }
            Cuenta c = registro.get(indiceActual);
            setTextos(c, indiceActual, registro.size());

//            conexion.close();
        } catch (SQLException ex) {
            System.out.println("Error al ejecutar la consulta" + ex);
        }
        return registro;
    }

    ArrayList getCUentasBalance() {
        ArrayList<Cuenta> cuentas = new ArrayList<Cuenta>();
        try {
            String query = "select *from Registro where saldo>" + 100 + "";
            buscarPorSaldo = conexion.prepareStatement(query);
            ResultSet resultado = buscarPorSaldo.executeQuery();
            while (resultado.next()) {
                Cuenta registro = new Cuenta();
                String numCu = resultado.getString("numeroCuenta");
                String client = resultado.getString("cliente");
                String tipCu = resultado.getString("tipoCuenta");
                String mov = resultado.getString("movimiento");
                String fecha = resultado.getString("fecha");
                String saldoo = resultado.getString("saldo");
                registro.setNumeroCuenta(numCu);
                registro.setCliente(client);
                registro.setTipo_Cuenta(tipCu);
                registro.setMovimiento(mov);
                registro.setFecha(fecha);
                registro.setSaldo(saldoo);
                cuentas.add(registro);

            }
            for (int i = 0; i < registro.size(); i++) {
                System.out.println(registro.get(i).getCliente());
            }
            Cuenta c = registro.get(indiceActual);
            setTextos(c, indiceActual, registro.size());

        } catch (SQLException ex) {
            System.out.println("Error al ejecutar la consulta" + ex);
        }
        return cuentas;
    }

