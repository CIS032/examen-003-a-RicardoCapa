    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String numCu = txtNumCuenta.getText();
        String client = txtCliente.getText();
        String tipCu = txtTipoCuenta.getText();
        String mov = txtMovimiento.getText();
        String fecha = txtFecha.getText();
        String saldo = txtSaldo.getText();

        try {
            PreparedStatement insertarRegistro = conexion.prepareStatement("INSERT INTO Registro (numeroCuenta,cliente,tipoCuenta,Movimiento,fecha,saldo) VALUES(?,?,?,?,?,?)");
            insertarRegistro.setString(1, numCu);
            insertarRegistro.setString(2, client);
            insertarRegistro.setString(3, tipCu);
            insertarRegistro.setString(4, mov);
            insertarRegistro.setString(5, fecha);
            insertarRegistro.setString(6, saldo);
            insertarRegistro.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos ingresados Correctamente", "Ingreso", JOptionPane.INFORMATION_MESSAGE);

        } catch (SQLException ex) {
            System.out.println("Agregar Persona " + ex);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

